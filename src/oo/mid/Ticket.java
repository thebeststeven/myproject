package oo.mid;

public class Ticket {
	int id;
	String name;
	float price;
	
	public void setID(int id){
		this.id=id;
	}
	
	public int getID(){
		return id;
	}
	
	public void setName(String name){
		this.name=name;
	}
	
	public String getName(){
		return name;
	}
	
	public void setPrice(float price){
		this.price=price;
	}
	
	public float getPrice(){
		return price;
	}

}
